// Reprensents state in AES
class State {
 public:
  State();
  State(State* state);
  State(std::vector<uint16_t>);
  uint16_t operator[] (const uint8_t &index);
  uint16_t GetState(const uint8_t &index_x, const uint8_t &index_y);
  uint16_t GetState(const uint8_t &index_x);
  void SetState(uint8_t index, uint16_t value);
  void SetState(uint8_t index_x, uint8_t index_y, uint16_t value);
  void SetBitsPerState(const uint8_t &bit);
  uint8_t GetBitsPerState();
  void Print();
 protected:
  std::array<uint16_t, 16> state_;
  // Stores the position of most significant bit in all 16 state
  uint8_t bits_per_state_;
};
// Round of AES and corresponding operations
// In this implementation, AddRoundKey operation before first round is added into the first round,
// and all other AddRoundKey is put into the next round.
// Current structure:
// Not Final Round:
//    AddRoundKey
//    SubBytes
//    ShiftRows
//    MixColumns
// Final Round:
//    AddRoundKey
//    SubBytes
//    ShiftRows
//    AddRoudKey
class Round {
 public:
  Round() = default;
  // Generates a round with round key w, it differs depending on if this round is final round
  Round(const std::vector<uint32_t> &w, const bool &is_final);
  // Multiplition operation on finit field GF(2^8)
  static uint8_t Gmult(uint8_t a, uint8_t b);
  void Encrypt(State *state);
  void Decrypt(State *state);
 private:
  // vector of encoding round stores parameter of corresponding function
  std::vector<std::vector<void*> > enc_param_vector_;
  // vector of encoding round stores functions
  std::vector<void (*)(std::vector<void*>)> enc_func_vector_;
  std::vector<std::vector<void*> > dec_param_vector_;
  std::vector<void (*)(std::vector<void*>)> dec_func_vector_;
  static void AddRoundKey(std::vector<void*>);
  static void SubBytes(std::vector<void*>);
  static void ShiftRows(std::vector<void*>);
  static void MixColumns(std::vector<void*>);
  static void InvAddRoundKey(std::vector<void*>);
  static void InvSubBytes(std::vector<void*>);
  static void InvShiftRows(std::vector<void*>);
  static void InvMixColumns(std::vector<void*>);
};
