  template<typename T>
  static void ShiftVec(std::vector<T> *vec, int group_size = 1) {
    std::vector<T> temp = *vec;
    for (int i = 0; i < 16; ++i) {
      for (int j = 0; j < group_size; ++j) {
        (*vec)[i*group_size + j] = temp[kShiftTab[i]*group_size + j];
      }
    }
  }
  template<typename T>
  static void InvShiftVec(std::vector<T> *vec, int group_size = 1) {
    std::vector<T> temp = *vec;
    for (int i = 0; i < 16; ++i) {
      for (int j = 0; j < group_size; ++j) {
        (*vec)[i*group_size + j] = temp[kInvShiftTab[i]*group_size + j];
      }
    }
  }
